#ifndef _RET_CODE_H
#define _RET_CODE_H

/******************************************************************************/
/* Boolean                                                                    */
/******************************************************************************/
#ifndef TRUE
#define TRUE                                      1
#endif

#ifndef FALSE
#define FALSE                                     0
#endif


/******************************************************************************/
/* Success                                                                    */
/******************************************************************************/
#define RET_OK                                    0

/******************************************************************************/
/* General error                                                              */
/******************************************************************************/
#define RET_GEN_ERROR                             1

/******************************************************************************/
/* Invalid parameter                                                          */
/******************************************************************************/
#define RET_INVALID_PARAM                         2

/******************************************************************************/
/* Memory error                                                               */
/******************************************************************************/
#define RET_MEMORY_ERROR                          3

/******************************************************************************/
/* Invalid option                                                             */
/******************************************************************************/
#define RET_INVALID_OPTION                        4

/******************************************************************************/
/* System error                                                               */
/******************************************************************************/
#define RET_SYS_ERROR                             5

/******************************************************************************/
/* File open error                                                            */
/******************************************************************************/
#define RET_FILE_OPEN_ERROR                       6

/******************************************************************************/
/* File write error                                                           */
/******************************************************************************/
#define RET_FILE_WRITE_ERROR                      7

/******************************************************************************/
/* File read error                                                            */
/******************************************************************************/
#define RET_FILE_READ_ERROR                       8

/******************************************************************************/
/* Socket open error                                                          */
/******************************************************************************/
#define RET_SOCK_OPEN_ERROR                       9

/******************************************************************************/
/* Socket write error                                                         */
/******************************************************************************/
#define RET_SOCK_WRITE_ERROR                      10

/******************************************************************************/
/* Socket read error                                                          */
/******************************************************************************/
#define RET_SOCK_READ_ERROR                       11

/******************************************************************************/
/* Process fork error                                                         */
/******************************************************************************/
#define RET_FORK_ERROR                            12

/******************************************************************************/
/* Malformed DNS request                                                      */
/******************************************************************************/
#define RET_MALFORMED_DNS_REQ                     13

/******************************************************************************/
/* Invalid config file statement                                              */
/******************************************************************************/
#define RET_INVALID_CONFIG                        14

/******************************************************************************/
/* Invalid DNS name.                                                          */
/******************************************************************************/
#define RET_INVALID_DNS_NAME                      15

/******************************************************************************/
/* Invalid Command.                                                           */
/******************************************************************************/
#define RET_INVALID_COMMAND                       16

/******************************************************************************/
/* Data not found.                                                            */
/******************************************************************************/
#define RET_DATA_NOT_FOUND                        17

#endif

